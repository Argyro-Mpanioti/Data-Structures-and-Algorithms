void avltree::left(node *node) //Συνάρτηση που υλοποιεί την αριστερή περιστροφή
{
    ::node * tempnode;
    tempnode=node->getrc();
    node->setrc(tempnode->getlc());
    if(tempnode->getlc()!=nullptr)
    {
        tempnode->getlc()->setpr(node);
    }
    tempnode->setlc(node);

    if(node->getvalue()!=root->getvalue()) //Αν ο κόμβος δεν είναι η ρίζα, δηλαδή αν ο κόμβος έχει γονέα
    {
        if(node->getpr()->getrc()!=nullptr) //Αν ο γονέας του κόμβου έχει δεξί παιδί
        {
            if(node->getpr()->getrc()->getvalue()==node->getvalue()) //Αν ο κόμβος είναι δεξί παιδί του γονέα του
            {
                node->getpr()->setrc(tempnode); //Κάνε δεξί παιδί του γονέα του κόμβου το δεξί παιδί του κόμβου
                tempnode->setpr(node->getpr());  
            }
            else
            {
                node->getpr()->setlc(tempnode); //Κάνε αριστερό παιδί του γονέα του κόμβου το δεξί παιδί του κόμβου
                tempnode->setpr(node->getpr());  
            }
        }
        else
        {
            node->getpr()->setlc(tempnode); //Κάνε αριστερό παιδί του γονέα του κόμβου το δεξί παιδί του κόμβου
            tempnode->setpr(node->getpr());  
        }
    }
    else //Αλλιώς, δηλαδή αν ο κόμβος είναι ρίζα, κάνε ρίζα το δεξί παιδί του κόμβου
    {
        root=tempnode;
        tempnode->setpr(nullptr);
    } 
    node->setpr(tempnode);
}


void avltree::right(node *node) //Συνάρτηση που υλοποιεί την δεξιά περιστροφή
{
    ::node* tempnode;
    tempnode=node->getlc();
    node->setlc(tempnode->getrc());
    if(tempnode->getrc()!=nullptr)
    {
        tempnode->getrc()->setpr(node);
    }
    tempnode->setrc(node);

    if(node->getvalue()!=root->getvalue()) //Αν ο κόμβος δεν είναι η ρίζα, δηλαδή αν ο κόμβος έχει γονέα
    {
        if(node->getpr()->getrc()!=nullptr)
        {
            if(node->getpr()->getrc()->getvalue()==node->getvalue()) //Αν ο κόμβος είναι δεξί παιδί του γονέα του
            {
                node->getpr()->setrc(tempnode); //Κάνε δεξί παιδί του γονέα του κόμβου το αριστερό παιδί του κόμβου
                tempnode->setpr(node->getpr());
            }    
        }
        if(node->getpr()->getlc()!=nullptr)
        {
            if(node->getpr()->getlc()->getvalue()==node->getvalue()) //Αν ο κόμβος είναι αριστερό παιδί του γονέα του
            {
                node->getpr()->setlc(tempnode); //Κάνε αριστερό παιδί του γονέα του κόμβου το αριστερό παιδί του κόμβου
                tempnode->setpr(node->getpr());
            }    
        }
    }
    else //Αλλιώς, δηλαδή αν ο κόμβος είναι ρίζα, κάνε ρίζα το αριστερό παιδί του κόμβου
    {
        root=tempnode;
        tempnode->setpr(nullptr);
    }   
    node->setpr(tempnode);
}


int avltree::foo(node *node, int height) //Συνάρτηση με την οποία βρίσκω το υψος του υποδένδρου με ρίζα τον κόμβο node.
{
    int h1,h2; //Στο h1 αποθηκεύουμε το ύψος του αριστερού υποδένδρου του node. Στο h2 αποθηκεύουμε το ύψος του δεξιού υποδένδρου του node
    if (node == nullptr) //Αν ο κόμβος είναι nullptr τότε επιστρέφω το ύψος που έχω υπολογίσει
    {
        return height;
    }
    else
    {
        h1=foo(node->getlc(),height+1); //Υπολογίζω με αναδρομική κλήση της foo το ύψος του αριστερού υποδένδρου του node.
        h2=foo(node->getrc(),height+1); //Υπολογίζω με αναδρομική κλήση της foo το ύψος του δεξιού υποδένδρου του node.
        
        //Με την παρακάτω if επιστρέφω το μεγαλύτερο από τα ύψη των υποδένδρων του κόμβου.
        if(h1>h2) 
        {
            return h1;
        }
        else
        {
            return h2;
        }
    }
}


void avltree::rotations(node *node) //H μεταβλητή balanced γίνεται true αν με την προηγούμενη κλήση της συνάρτησης rotations το δένδρο έγινε δένδρο AVL
{

    int t1,t2; //Στη μεταβλητή t1 αποθηκεύω το ύψος του δεξιού υποδένδρου του κόμβου. Στη μεταβλητή t2 αποθηκεύω το ύψος του αριστερού υποδένδρου του κόμβου.
    bool balanced=false;

    while(node!=nullptr && balanced!=true) //Όσο ο κόμβος node δεν είναι κενός και με την προηγούμενη κλήση της συνάρτησης rotations το δένδρο δεν έγινε δένδρο AVL εξετάζουμε αν χρειάζεται κάποια περιστροφή το δένδρο με ρίζα τον κόμβο node
    {
        t1=foo(node->getrc(),0); //Εκχωρώ στο t1 το ύψος του δεξιού υποδένδρου του κόμβου με κλήση της συνάρτησης foo με παράμετρο το δεξί παιδί του κόμβου και το 0
        t2=foo(node->getlc(),0); //Εκχωρώ στο t2 το ύψος του δεξιού υποδένδρου του κόμβου με κλήση της συνάρτησης foo με παράμετρο το δεξί παιδί του κόμβου και το 0
        
        if(t1>t2) //Αν το ύψος του δεξιού υποδένδρου του κόμβου είναι μεγαλύτερο από το ύψος του αριστερού υποδένδρου του κόμβου
        {
            if(t1-t2>=2) //Αν χρειάζεται να γίνει κάποια περιστροφή
            {
                balanced=true; //Μετά την περιστροφή το δένδρο θα είναι AVL
                //Αν το δεξί υποδένδρο του δεξιού παιδιού του κόμβου έχει μεγαλύτερο ύψος από το αριστερό υποδένδρο του δεξιού παιδιού του κόμβου τότε αρκεί να γίνει μια απλή αριστερή περιστροφή
                if(foo(node->getrc()->getrc(),0)>foo(node->getrc()->getlc(),0))
                {
                    left(node);
                }
                else //Αλλιώς χρειάζεται να κάνουμε μία διπλή περιστροφή right left
                {
                    right(node->getrc());
                    left(node);
                }
            }
        }
        else if(t1<t2)//Αν το ύψος του δεξιού υποδένδρου του κόμβου είναι μικρότερο
        {
            if(t2-t1>=2) //Αν χρειάζεται να γίνει κάποια περιστροφή
            {
                balanced=true; //Μετά την περιστροφή το δένδρο θα είναι AVL
                //Αν το αριστερό υποδένδρο του αριστερού παιδιού του κόμβου έχει μεγαλύτερο ύψος από το δεξί υποδένδρο του αριστερού παιδιού του κόμβου τότε αρκεί να γίνει μια απλή δεξιά περιστροφή
                if(foo(node->getlc()->getlc(),0)>foo(node->getlc()->getrc(),0))
                {
                    right(node);
                }
                else //Αλλιώς χρειάζεται να κάνουμε μία διπλή περιστροφή left right
                {
                    left(node->getlc());
                    right(node);
                }
            }
        }
        node=node->getpr(); //Επαναλαμβάνουμε τη διαδικασία για τον γονέα του κόμβου τον οποίο μόλις εξετάσαμε
    }
    return;
}


void avltree::build(const char *filename) //Υλοποιούμε μία συνάρτηση με παράμετρο το όνομα του αρχείου από το οποίο δέχεται τα δεδομένα
{
    ifstream g;
    int value;
    g.open(filename);//Ανοίγουμε το αρχείο filename από το οποίο θα διαβάσουμε τα δεδομένα
    
    if (g.is_open ()) //Ελέγχουμε αν το αρχείο άνοιξε επιτυχώς
    {
        size=0;
        root=nullptr;
         
        while(g>>value)
        {
            insert(value); //Εισάγουμε την τιμή που διαβάσαμε από το αρχείο στο δένδρο
        }
        g.close(); //Κλείνουμε το αρχείο


    }
    else
    {
        cerr<<"Couldn't open file."; //Αν το αρχείο δεν μπορεί να ανοίξει επιτυχώς εμφανίζουμε ένα μήνυμα σφάλαμτος στην οθόνη
    }
}


